/**
 * Displays a welcome message
 */
public class MultiGreet
{
    public static void main(String[] args) {
        System.out.print("Hello\t");    //Print – without return “Hello”
        System.out.print("Buenos Dias\t");  //Print – without return “Buenos Dias”
        System.out.println("Bonjour");  //Print – with return “Bonjour”
        System.out.println("How do you greet each other?"); //Print – With Return “How do you greet each other?”
    }
}
